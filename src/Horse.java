
public class Horse {
    private String name;
    private Color color;
    private int birth;
    public void setName(String name){
        this.name = name;
    }
    public void setColor(Color color){
        this.color = color;
    }
    public void setBirth(int birth){
        this.birth= birth;
    }
    public String getName(){
        return this.name;
    }
    public Color getColor(){
        return this.color;
    }
    public int getBirth(){
        return this.birth;
    }
}
